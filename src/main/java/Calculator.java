public class Calculator {

    public double sum(double a, double b) {
        double res = a + b;
        return res;
    }

    public double res(double a, double b) {
        double res = a - b;
        return res;
    }

    public double div(double a, double b) {
        double res = a / b;
        return res;
    }

    public double mult(double a, double b) {
        double res = a*b;
        return res;
    }

}
